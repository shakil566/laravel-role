    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {

        // $posts = User::find(1)->role;
        // $posts = User::with('role')->get();
        // return $posts;
        $qpArr = $request->all();
        $targetArr = User::with('role')->orderBy('id', 'desc');
        $titleArr = User::select('name')->orderBy('id', 'desc')->get();

        //begin filtering
        $searchText = $request->search;
        if (!empty($searchText)) {
            $targetArr->where(function ($query) use ($searchText) {
                $query->where('name', 'LIKE', '%' . $searchText . '%');
            });
        }
        //end filtering

        $targetArr = $targetArr->paginate();

        return view('admin.user.index')->with(compact('targetArr', 'qpArr', 'titleArr'));
    }

    public function filter(Request $request)
    {
        $url = 'search=' . urlencode($request->search);
        return Redirect::to('/admin/user?' . $url);
    }

